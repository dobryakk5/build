def set_cell_text(cell, text: str) -> None:
    cell.text = ""
    paragraph = cell.paragraphs[0]
    paragraph.alignment = WD_ALIGN_PARAGRAPH.LEFT
    run = paragraph.add_run(text)
    run.font.name = "Arial"
    run.font.size = Pt(8.7)
    paragraph.paragraph_format.space_after = Pt(0)
    paragraph.paragraph_format.line_spacing = 1.05


def main() -> None:
    OUTPUT.parent.mkdir(parents=True, exist_ok=True)
    doc = Document(SOURCE)

    for paragraph in doc.paragraphs:
        if paragraph.text.strip():
            for run in paragraph.runs:
                run.font.name = "Arial"
            if paragraph.text.strip().startswith("В программе"):
                paragraph.paragraph_format.space_after = Pt(6)
            if paragraph.text.strip() in {"КТП", "ГПР"}:
                paragraph.paragraph_format.page_break_before = True
                paragraph.paragraph_format.space_before = Pt(0)

    for table, answers in zip(doc.tables, ANSWERS, strict=True):
        table.autofit = True
        for row_index, answer in enumerate(answers):
            set_cell_text(table.cell(row_index, 2), answer)

    doc.save(OUTPUT)
    print(OUTPUT)


if __name__ == "__main__":
    main()
