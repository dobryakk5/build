@dataclass(frozen=True)
class NwRule:
    pattern: str                                  # regex
    nw_code: str                                  # forced NW
    primary: bool = True
    confidence: str = "high"                      # high | medium
    mapping_type: str = "direct"                  # direct | partial
    only_in_collections: frozenset[int] | None = None
    note: str = ""

    def __post_init__(self):
        # компиляция регекспа кэшируется на module-level dict
        _COMPILED.setdefault(self.pattern, re.compile(self.pattern, re.IGNORECASE))


_COMPILED: dict[str, re.Pattern] = {}


def _co(*nums: int) -> frozenset[int]:
    return frozenset(nums)


